/* Estructura VBE Mode Info según especificación VBE 2.0 */
typedef struct __attribute__((packed)) {
    u8  attributes;
    u8  winA, winB;
    u16 granularity, winsize;
    u16 segA, segB;
    u32 winfunc_ptr;
    u16 pitch;
    u16 Xres, Yres;
    u8  Wchar, Ychar, planes, bpp, banks;
    u8  memory_model, bank_size, image_pages;
    u8  reserved0;
    u8  red_mask, red_position;
    u8  green_mask, green_position;
    u8  blue_mask, blue_position;
    u8  rsv_mask, rsv_position;
    u8  directcolor_attributes;
    u32 physbase;     /* framebuffer físico */
    u32 offscreen_mem_off;
    u16 offscreen_mem_size;
} vbe_mode_info_t;

/**
 * Detectar información VBE desde la estructura Multiboot
 * GRUB pasa esta información cuando se usa el flag apropiado
 */
void vbe_detect_from_multiboot(void* mbi_raw) {
    if (!mbi_raw) {
        vbe_current.active = 0;
        return;
    }
    
    /* Cast a la estructura multiboot oficial */
    multiboot_info_t* mbi = (multiboot_info_t*)mbi_raw;
    
    /* Verificar bit 11 de flags: información VBE presente */
    if (!(mbi->flags & (1 << 11))) {
        vbe_current.active = 0;
        return;
    }
    
    /* Verificar que el puntero a modo info no sea nulo */
    if (!mbi->vbe_mode_info) {
        vbe_current.active = 0;
        return;
    }
    
    /* Obtener información del modo VBE */
    vbe_mode_info_t* mi = (vbe_mode_info_t*)(u32)mbi->vbe_mode_info;
    
    /* Verificar que el framebuffer esté mapeado */
    if (!mi->physbase) {
        vbe_current.active = 0;
        return;
    }
    
    /* Solo soportamos modos de color directo (24bpp o 32bpp) */
    if (mi->bpp != 24 && mi->bpp != 32) {
        vbe_current.active = 0;
        return;
    }
    
    /* Verificar resolución mínima razonable */
    if (mi->Xres < 640 || mi->Yres < 480) {
        vbe_current.active = 0;
        return;
    }
    
    /* Guardar información del modo */
    vbe_current.width       = mi->Xres;
    vbe_current.height      = mi->Yres;
    vbe_current.bpp         = mi->bpp;
    vbe_current.framebuffer = mi->physbase;
    vbe_current.pitch       = mi->pitch;
    vbe_current.active      = 1;
}

/**
 * Mapear el framebuffer VBE en el espacio de direcciones del kernel
 * Esto es necesario cuando el framebuffer está en una dirección física alta
 */
int vbe_map_framebuffer(void) {
    if (!vbe_current.active) return -1;
    
    u32 fb_phys = vbe_current.framebuffer;
    u32 fb_size = vbe_current.height * vbe_current.pitch;
    
    /* Calcular número de páginas necesarias */
    u32 pages = (fb_size + PAGE_SIZE - 1) / PAGE_SIZE;
    
    /* Mapear cada página del framebuffer */
    for (u32 i = 0; i < pages; i++) {
        u32 phys_addr = fb_phys + (i * PAGE_SIZE);
        if (map_page(phys_addr, phys_addr, PAGE_PRESENT | PAGE_WRITABLE) != 0) {
            return -1;
        }
    }
    
    return 0;
}

u32 vbe_rgb(u8 r, u8 g, u8 b) { 
    return ((u32)r << 16) | ((u32)g << 8) | b; 
}

void vbe_set_pixel(int x, int y, u32 color) {
    if (!vbe_current.active) return;
    if (x < 0 || y < 0 || x >= vbe_current.width || y >= vbe_current.height) return;
    
    u32 off = y * vbe_current.pitch + x * (vbe_current.bpp / 8);
    u8* fb = (u8*)(u32)vbe_current.framebuffer;
    
    fb[off + 0] = color & 0xFF;
    fb[off + 1] = (color >> 8) & 0xFF;
    fb[off + 2] = (color >> 16) & 0xFF;
    
    /* En modo 32bpp, el byte alto es el canal alfa (0xFF = opaco) */
    if (vbe_current.bpp == 32) {
        fb[off + 3] = 0xFF;
    }
}

void vbe_fill(u32 color) {
    if (!vbe_current.active) return;
    
    u8* fb = (u8*)(u32)vbe_current.framebuffer;
    u32 pitch = vbe_current.pitch;
    u32 width = vbe_current.width;
    u32 height = vbe_current.height;
    u32 bpp_bytes = vbe_current.bpp / 8;
    
    /* Optimización: escribir línea por línea */
    for (u32 y = 0; y < height; y++) {
        u8* line = fb + y * pitch;
        for (u32 x = 0; x < width; x++) {
            u32 off = x * bpp_bytes;
            line[off + 0] = color & 0xFF;
            line[off + 1] = (color >> 8) & 0xFF;
            line[off + 2] = (color >> 16) & 0xFF;
            if (bpp_bytes == 4) {
                line[off + 3] = 0xFF;
            }
        }
    }
}

void vbe_rect(int x, int y, int w, int h, u32 color) {
    if (!vbe_current.active) return;
    
    /* Verificar límites */
    if (x < 0) { w += x; x = 0; }
    if (y < 0) { h += y; y = 0; }
    if (x + w > vbe_current.width) w = vbe_current.width - x;
    if (y + h > vbe_current.height) h = vbe_current.height - y;
    if (w <= 0 || h <= 0) return;
    
    u8* fb = (u8*)(u32)vbe_current.framebuffer;
    u32 pitch = vbe_current.pitch;
    u32 bpp_bytes = vbe_current.bpp / 8;
    
    /* Optimización: escribir línea por línea */
    for (int dy = 0; dy < h; dy++) {
        u8* line = fb + (y + dy) * pitch + x * bpp_bytes;
        for (int dx = 0; dx < w; dx++) {
            u32 off = dx * bpp_bytes;
            line[off + 0] = color & 0xFF;
            line[off + 1] = (color >> 8) & 0xFF;
            line[off + 2] = (color >> 16) & 0xFF;
            if (bpp_bytes == 4) {
                line[off + 3] = 0xFF;
            }
        }
    }
}

void vbe_char(int x, int y, char c, u32 fg, u32 bg) {
    if (!vbe_current.active) return;
    
    const u8* glyph = font8x16[(u8)c];
    u8* fb = (u8*)(u32)vbe_current.framebuffer;
    u32 pitch = vbe_current.pitch;
    u32 bpp_bytes = vbe_current.bpp / 8;
    
    for (int row = 0; row < 16; row++) {
        u8 bits = glyph[row];
        u8* line = fb + (y + row) * pitch + x * bpp_bytes;
        
        for (int col = 0; col < 8; col++) {
            u32 color = (bits & (0x80 >> col)) ? fg : bg;
            u32 off = col * bpp_bytes;
            
            line[off + 0] = color & 0xFF;
            line[off + 1] = (color >> 8) & 0xFF;
            line[off + 2] = (color >> 16) & 0xFF;
            if (bpp_bytes == 4) {
                line[off + 3] = 0xFF;
            }
        }
    }
}

void vbe_print(int x, int y, const char* s, u32 fg, u32 bg) {
    int cx = x;
    while (*s) {
        if (*s == '\n') { y += 16; cx = x; }
        else { vbe_char(cx, y, *s, fg, bg); cx += 8; }
        s++;
    }
}

void vbe_print_int(int x, int y, int n, u32 fg, u32 bg) {
    char buf[12]; 
    int i = 10; 
    buf[11] = 0;
    
    if (n == 0) { vbe_char(x, y, '0', fg, bg); return; }
    if (n < 0)  { vbe_char(x, y, '-', fg, bg); x += 8; n = -n; }
    
    while (n > 0) { 
        buf[i--] = '0' + (n % 10); 
        n /= 10; 
    }
    vbe_print(x, y, buf + i + 1, fg, bg);
}

int vbe_cols(void) { 
    return vbe_current.active ? vbe_current.width / 8 : 80; 
}

int vbe_rows(void) { 
    return vbe_current.active ? vbe_current.height / 16 : 25; 
}
